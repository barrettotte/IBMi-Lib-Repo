CREATE OR REPLACE VIEW BOLIB.EMPTY_INVENTORY FOR SYSTEM NAME EMPTYINV ( 

	PARTNO , 

	DESCR ) 

	AS 

	SELECT PARTNO, DESCR FROM BOLIB.INVENTORY 

	 WHERE QONHAND = 0   

	RCDFMT EMPTYINV   ; 

  

GRANT DELETE , INSERT , SELECT , UPDATE   

ON BOLIB.EMPTY_INVENTORY TO PUBLIC ; 

  

GRANT ALTER , DELETE , INSERT , REFERENCES , SELECT , UPDATE   

ON BOLIB.EMPTY_INVENTORY TO QPGMR WITH GRANT OPTION ; 

  
