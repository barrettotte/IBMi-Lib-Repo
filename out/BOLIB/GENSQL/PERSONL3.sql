CREATE OR REPLACE VIEW BOLIB.PERSONL3 ( 

--  SQL1506   30   Key or attribute for PERSONL3 in BOLIB ignored. 

	PERSONID , 

	FIRSTNAME , 

	LASTNAME , 

	AGE ) 

	AS 

	SELECT 

	PERSONID , 

	FIRSTNAME , 

	LASTNAME , 

	AGE   

	FROM BOLIB.PERSON 

	WHERE 

	( ( NOT AGE > +30 ) OR AGE IS NULL )   

	RCDFMT PERSFMT3   ; 

  

LABEL ON TABLE BOLIB.PERSONL3 

	IS 'View over person table on age > 30' ; 

  

GRANT DELETE , INSERT , SELECT , UPDATE   

ON BOLIB.PERSONL3 TO PUBLIC ; 

  

GRANT ALTER , DELETE , INSERT , REFERENCES , SELECT , UPDATE   

ON BOLIB.PERSONL3 TO QPGMR WITH GRANT OPTION ; 

  
